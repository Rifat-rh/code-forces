/*  Say Hello with c++ :
Given a name S. Print "Hello, (name)" without parentheses.

Input
Only one line containing a string S.

Output
Print "Hello, " without quotes, then print name.
*/
#include<bits/stdc++.h>
using namespace std;

int main(){
    char n[100];
    cin>>n;
    cout<<"Hello,"<<" "<<n;
    return 0;
}